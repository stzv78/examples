     * Handle the user "updated" event.
     *
     * @param  \App\User  $user
     * @return void
     */
    public function updating(User $user)
    {
        if($user->points >= 150) {
            $user->level = 2;
            return true;
        }

        if($user->points >= 50) {
            $user->level = 1;
            return true;
        }
        
    }

